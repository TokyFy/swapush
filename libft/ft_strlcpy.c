#include "libft.h"

unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	t_size_t	lsrc;

	lsrc = ft_strlen(src);
	if (lsrc + 1 < size)
		ft_memcpy(dest, src, lsrc + 1);
	else if (size != 0)
	{
		ft_memcpy(dest, src, size - 1);
		dest[size - 1] = '\0';
	}
	return (lsrc);
}
