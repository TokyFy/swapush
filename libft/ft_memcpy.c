#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, t_size_t n)
{
	char	*ptr;

	if (!dest && !src)
		return (0);
	ptr = dest;
	while (n-- != 0)
	{
		*ptr++ = *(char *)src++;
	}
	return (dest);
}
