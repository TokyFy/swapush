#include "libft.h"
#include <stdlib.h>

void	*ft_calloc(t_size_t nmemb, t_size_t size)
{
	void	*ptr;

	ptr = malloc(nmemb * size);
	if (!ptr)
		return (0);
	ft_bzero(ptr, nmemb * size);
	return (ptr);
}
